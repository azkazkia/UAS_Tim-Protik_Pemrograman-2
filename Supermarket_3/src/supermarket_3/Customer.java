    private void tblCustomerMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomerMousePressed
        // TODO add your handling code here:
        int lastColumnHidden = tblCustomer.getColumnModel().getColumnCount();
        this.ID = (String) tblCustomer.getModel().getValueAt(tblCustomer.getSelectedRow(), lastColumnHidden);
        String first_name = (String) tblCustomer.getModel().getValueAt(tblCustomer.getSelectedRow(), 1);
        String last_name = (String) tblCustomer.getModel().getValueAt(tblCustomer.getSelectedRow(), 2);
        String phone = (String) tblCustomer.getModel().getValueAt(tblCustomer.getSelectedRow(), 3);
        String address = (String) tblCustomer.getModel().getValueAt(tblCustomer.getSelectedRow(), 4);
        String join = (String) tblCustomer.getModel().getValueAt(tblCustomer.getSelectedRow(), 5);
        String money_spent = (String) tblCustomer.getModel().getValueAt(tblCustomer.getSelectedRow(), 6);
        String money_spent_reset = (String) tblCustomer.getModel().getValueAt(tblCustomer.getSelectedRow(), 7);
        
        SimpleDateFormat inputJoinFormat = new SimpleDateFormat(inputJoinDate.getDateFormatString());
        
        inputFirstName.setText(first_name);
        inputLastName.setText(last_name);
        inputPhone.setText(phone);
        inputAddress.setText(address);
        try {
            inputJoinDate.setDate(inputJoinFormat.parse(join));
        } catch (ParseException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
        inputMoneySpent.setText(money_spent);
        inputMoneySpentReset.setText(money_spent_reset);
    }//GEN-LAST:event_tblCustomerMousePressed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String firstName, lastName, phone, address, joinDate, moneySpent, moneySpentReset;
        firstName = inputFirstName.getText();
        lastName = inputLastName.getText();
        phone = inputPhone.getText();
        address = inputAddress.getText();
        joinDate = String.valueOf(ft.format(inputJoinDate.getDate()));
        moneySpent = inputMoneySpent.getText();
        moneySpentReset = inputMoneySpentReset.getText();
        if (!this.ID.isEmpty()) {
            if(firstName.isEmpty() || lastName.isEmpty() || phone.isEmpty() || address.isEmpty() || joinDate.isEmpty() || moneySpent.isEmpty() || moneySpentReset.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Mohon isi seluruh data.", "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else {
                java.sql.Connection con = new Connection().getConnection();
                String sql = "UPDATE customer SET first_name = ?, last_name = ?, cphone = ?, caddress = ?, cjoin_date = ?, cmoney_spent = ?, cmoney_spent_reset = ? WHERE cid = ?";
                try {
                    PreparedStatement pst = con.prepareStatement(sql);
                    pst.setString(1, firstName);
                    pst.setString(2, lastName);
                    pst.setString(3, phone);
                    pst.setString(4, address);
                    pst.setString(5, joinDate);
                    pst.setString(6, moneySpent);
                    pst.setString(7, moneySpentReset);
                    pst.setString(8, this.ID);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil diubah");
                    showCustomer();
                    reset();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Data gagal diubah");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Anda belum memilih data.", "Warning", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (!this.ID.isEmpty()) {
            int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data yang dipilih?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if(ok==0) {
                java.sql.Connection con = new Connection().getConnection();
                String sql = "DELETE FROM customer WHERE cid = ?";
                try {
                    PreparedStatement pst = con.prepareStatement(sql);
                    pst.setString(1, this.ID);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                    showCustomer();
                    reset();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Data gagal dihapus");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Anda belum memilih data.", "Warning", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

