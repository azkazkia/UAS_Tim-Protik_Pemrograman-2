    private void showCustomer() {
        Object [] baris = {"No.", "Nama Depan", "Nama Belakang", "No. HP", "Alamat", "Join", "Uang Keluar", "Reset", "HIDDEN_ID"};
        supplierModel = new DefaultTableModel(null, baris){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblCustomer.setModel(supplierModel);
        tblCustomer.setRowHeight(20);
        
        java.sql.Connection con = new Connection().getConnection();
        try {
            String sql = "SELECT * FROM customer";
            Statement stat = con.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            int num = 1;
            while(hasil.next()) {
                String id = hasil.getString("cid");
                String first_name = hasil.getString("first_name");
                String last_name = hasil.getString("last_name");
                String phone = hasil.getString("cphone");
                String address = hasil.getString("caddress");
                String join_date = hasil.getString("cjoin_date");
                String money_spent = hasil.getString("cmoney_spent");
                String money_spent_reset = hasil.getString("cmoney_spent_reset");
                String[] data = {String.valueOf(num), first_name, last_name, phone, address, join_date, money_spent, money_spent_reset, id};
                supplierModel.addRow(data);
                num++;
            }
                    
            tblCustomer.removeColumn(tblCustomer.getColumnModel().getColumn(8));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal menampilkan data customer.", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
