    private void getSupplier(){
        inputSupplier.removeAllItems();
        supplier.clear();
        try {
            java.sql.Connection con = new Connection().getConnection();
            String sqlServices = "SELECT * FROM supplier";
            Statement stat = con.createStatement();
            ResultSet hasil = stat.executeQuery(sqlServices);
            while(hasil.next()) {
                inputSupplier.addItem(hasil.getString("sdealer")+" - "+hasil.getString("sname"));
                supplier.put(hasil.getString("sdealer")+" - "+hasil.getString("sname"), hasil.getString("sid"));
            }
            stat.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menampilkan data supplier.", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }

