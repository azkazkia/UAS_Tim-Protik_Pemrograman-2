    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String name, buy, sale, qty, type, supplier_id;
        name = inputName.getText();
        buy = inputBuy.getText();
        sale = inputSale.getText();
        qty = inputQty.getText();
        type = inputType.getSelectedItem().toString();
        supplier_id = supplier.get(inputSupplier.getSelectedItem().toString());
        if (!this.ID.isEmpty()) {
            if(name.isEmpty() || buy.isEmpty() || sale.isEmpty() || qty.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Mohon isi seluruh data.", "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else {

                java.sql.Connection con = new Connection().getConnection();
                String sql = "UPDATE product SET product_name = ?, cost_price = ?, market_price = ?, quantity = ?, product_type = ?, supplier_id = ? WHERE product_id = ?";
                try {
                    PreparedStatement pst = con.prepareStatement(sql);
                    pst.setString(1, name);
                    pst.setString(2, buy);
                    pst.setString(3, sale);
                    pst.setString(4, qty);
                    pst.setString(5, type);
                    pst.setString(6, supplier_id);
                    pst.setString(7, this.ID);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil diubah");
                    showProduct();
                    reset();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Data gagal diubah");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Anda belum memilih data.", "Warning", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (!this.ID.isEmpty()) {
            int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data yang dipilih?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if(ok==0) {
                java.sql.Connection con = new Connection().getConnection();
                String sql = "DELETE FROM product WHERE product_id = ?";
                try {
                    PreparedStatement pst = con.prepareStatement(sql);
                    pst.setString(1, this.ID);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                    showProduct();
                    reset();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Data gagal dihapus");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Anda belum memilih data.", "Warning", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblProductMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductMousePressed
        // TODO add your handling code here:
        int lastColumnHidden = tblProduct.getColumnModel().getColumnCount();
        this.ID = (String) tblProduct.getModel().getValueAt(tblProduct.getSelectedRow(), lastColumnHidden);
        
        String name = (String) tblProduct.getModel().getValueAt(tblProduct.getSelectedRow(), 1);
        String price_cost = (String) tblProduct.getModel().getValueAt(tblProduct.getSelectedRow(), 2);
        String price_market = (String) tblProduct.getModel().getValueAt(tblProduct.getSelectedRow(), 3);
        String quantity = (String) tblProduct.getModel().getValueAt(tblProduct.getSelectedRow(), 4);
        String type = (String) tblProduct.getModel().getValueAt(tblProduct.getSelectedRow(), 5);
        String supplier = (String) tblProduct.getModel().getValueAt(tblProduct.getSelectedRow(), 6);
        
        inputName.setText(name);
        inputBuy.setText(price_cost);
        inputSale.setText(price_market);
        inputQty.setText(quantity);
        inputType.setSelectedItem(type);
        inputSupplier.setSelectedItem(supplier);
    }//GEN-LAST:event_tblProductMousePressed

