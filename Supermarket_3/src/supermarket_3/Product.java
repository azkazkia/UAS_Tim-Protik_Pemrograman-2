    private void showProduct() {
        Object [] baris = {"No.", "Nama", "Harga", "Harga Pasar", "QTY", "Jenis", "Supplier", "HIDDEN_ID"};
        productModel = new DefaultTableModel(null, baris){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblProduct.setModel(productModel);
        tblProduct.setRowHeight(20);
        
        java.sql.Connection con = new Connection().getConnection();
        try {
            String sql = "SELECT product.*, supplier.sname, supplier.sdealer FROM product LEFT JOIN supplier ON supplier.sid = product.supplier_id";
            Statement stat = con.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            int num = 1;
            while(hasil.next()) {
                String id = hasil.getString("product_id");
                String name = hasil.getString("product_name");
                String buy = hasil.getString("cost_price");
                String sale = hasil.getString("market_price");
                String qty = hasil.getString("quantity");
                String type = hasil.getString("product_type");
                String supplier_name = hasil.getString("sname");
                String supplier_dealer = hasil.getString("sdealer");
                String[] data = {String.valueOf(num), name, buy, sale, qty, type, supplier_dealer+" - "+supplier_name, id};
                productModel.addRow(data);
                num++;
            }
                    
            tblProduct.removeColumn(tblProduct.getColumnModel().getColumn(7));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal menampilkan data product.", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
