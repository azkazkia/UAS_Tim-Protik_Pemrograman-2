    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String dealer, name, email, phone, address;
        dealer = inputDealer.getText();
        name = inputName.getText();
        email = inputEmail.getText();
        phone = inputPhone.getText();
        address = inputAddress.getText();
        if(dealer.isEmpty() || name.isEmpty() || email.isEmpty() || phone.isEmpty() || address.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Mohon isi seluruh data.", "Warning", JOptionPane.INFORMATION_MESSAGE);
        } else {
            
            java.sql.Connection con = new Connection().getConnection();
            String sql = "INSERT INTO supplier(sdealer, sname, semail, sphone, saddress) VALUES (?, ?, ?, ?, ?)";
            try {
                PreparedStatement pst = con.prepareStatement(sql);
                pst.setString(1, dealer);
                pst.setString(2, name);
                pst.setString(3, email);
                pst.setString(4, phone);
                pst.setString(5, address);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                showSupplier();
                reset();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan");
            }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

