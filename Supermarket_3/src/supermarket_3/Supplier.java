    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String dealer, name, email, phone, address;
        dealer = inputDealer.getText();
        name = inputName.getText();
        email = inputEmail.getText();
        phone = inputPhone.getText();
        address = inputAddress.getText();
        if (!this.ID.isEmpty()) {
            if(dealer.isEmpty() || name.isEmpty() || email.isEmpty() || phone.isEmpty() || address.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Mohon isi seluruh data.", "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else {
                
                java.sql.Connection con = new Connection().getConnection();
                String sql = "UPDATE supplier SET sdealer = ?, sname = ?, semail = ?, sphone = ?, saddress = ? WHERE sid = ?";
                try {
                    PreparedStatement pst = con.prepareStatement(sql);
                    pst.setString(1, dealer);
                    pst.setString(2, name);
                    pst.setString(3, email);
                    pst.setString(4, phone);
                    pst.setString(5, address);
                    pst.setString(6, this.ID);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil diubah");
                    showSupplier();
                    reset();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Data gagal diubah");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Anda belum memilih data.", "Warning", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (!this.ID.isEmpty()) {
            int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data yang dipilih?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if(ok==0) {
                java.sql.Connection con = new Connection().getConnection();
                String sql = "DELETE FROM supplier WHERE sid = ?";
                try {
                    PreparedStatement pst = con.prepareStatement(sql);
                    pst.setString(1, this.ID);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                    showSupplier();
                    reset();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Data gagal dihapus");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Anda belum memilih data.", "Warning", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

