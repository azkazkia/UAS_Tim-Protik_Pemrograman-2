    private void showSupplier() {
        Object [] baris = {"No.", "Dealer", "Nama", "Email", "No. HP", "Alamat", "HIDDEN_ID"};
        supplierModel = new DefaultTableModel(null, baris){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblSupplier.setModel(supplierModel);
        tblSupplier.setRowHeight(20);
        
        java.sql.Connection con = new Connection().getConnection();
        try {
            String sql = "SELECT * FROM supplier";
            Statement stat = con.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            int num = 1;
            while(hasil.next()) {
                String id = hasil.getString("sid");
                String dealer = hasil.getString("sdealer");
                String name = hasil.getString("sname");
                String email = hasil.getString("semail");
                String phone = hasil.getString("sphone");
                String address = hasil.getString("saddress");
                String[] data = {String.valueOf(num), dealer, name, email, phone, address, id};
                supplierModel.addRow(data);
                num++;
            }
                    
            tblSupplier.removeColumn(tblSupplier.getColumnModel().getColumn(6));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal menampilkan data supplier.", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
